# Igual a (==)
is_equal = 5 == 6
print(f"¿5 es igual a 6?: {is_equal}")

# No es igual a (!=)
is_not_equal = 5 != 6
print(f"¿5 NO es igual a 6?: {is_not_equal}")

# Mayor que (>)
is_greater = 5 > 6
print(f"¿5 es mayor que 6?: {is_greater}")

# Menor que (<)
is_minor = 5 < 6
print(f"¿5 es menor que 6?: {is_minor}")

# Mayor o igual a (>=)
is_greater_or_equal = 5 >= 6
print(f"¿5 es mayor o igual a 6?: {is_greater_or_equal}")

# Menor o igual a (<=)
is_minor_or_equal = 5 <= 6
print(f"¿5 es menor o igual a 6?: {is_minor_or_equal}")

# Comparando cadenas
