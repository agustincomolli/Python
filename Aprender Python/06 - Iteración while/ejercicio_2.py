print("\nPaso 1: \n********")

contar = 0
while contar != 5:
    contar += 1
    print(contar)

# Incrementar el recuento en 3
print("\nPaso 2: \n********")

contar = 0
while contar <= 20:
    contar += 3
    print(contar)

# Restar 3 al recuento
print("\nPaso 2: \n********")

contar = 20
while contar >= 0:
    contar -= 3
    print(contar)

