# Definir una tupla con las dimensiones de un rectángulo.
print("\nRectángulo:")
dimensiones = (200, 50)
print("Largo: " + str(dimensiones[0]))
print("Alto: " + str(dimensiones[1]))

# Iterar por los items de una tupla.
print("\nDimensiones originales:")
for dimension in dimensiones:
    print(dimension)

dimensiones = (400, 100 )
print("\nDimensiones modificadas:")
for dimension in dimensiones:
    print(dimension)